		<p style="text-align:center;font-weight:bolder">十七、其他事项</p>
		<p style="text-indent:2em">1、《认购风险申明书》与《投资计划说明书》是本合同不可分割的组成部分，和本合同具有同等法律效力。本合同未规定的，以《投资计划说明书》为准；如果本合同与《投资计划说明书》所规定的内容冲突，以本合同约定为准。</p>
