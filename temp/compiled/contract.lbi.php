		<p style="text-indent:2em">发生下列不可抗力情形时，当事人对于因下列原因而引起的损失可以免于承担相应责任：</p>
		<p style="text-indent:2em">1、地震、台风、水灾、火灾、战争等不可抗力之情况。</p>
		<p style="text-indent:2em">2、新的法律、法规的颁布、实施和现行的法律、法规的修改或有权机构对现行法律、法规的解释的变动。</p>
		<p style="text-indent:2em">3、国家的政治、经济、金融等状况发生重大变化。</p>
		<br>
		<p style="text-align:center;font-weight:bolder">十四、税收处理</p>
		<p style="text-indent:2em">认购人与发行人应按有关法律规定各自依法纳税。</p>
		<p style="text-indent:2em">应当由投资财产承担的税费，按照法律、法规及国家有关部门的规定办理。</p>
		<br>
		<p style="text-align:center;font-weight:bolder">十五、通知与送达</p>
		<p style="text-indent:2em"><b>（一）地址变更的通知</b></p>
		<p style="text-indent:2em">认购人的通讯地址或联系方式以认购人在“政金网”注册填写/认购风险申明书中填写的内容为准。认购人通讯地址或联系方式发生变化的，应及时在“政金网”平台上进行修改/以书面形式在发生变化后的3个工作日内通知受托管理人或发行人。未经告知的，不得以此变更对抗发行人。</p>
		<p style="text-indent:2em">由于认购人的原因造成通知不能送达或送达有误的，所产生的后果由认购人自行承担。</p>
		<p style="text-indent:2em">发行人及受托管理人通讯地址、联系方式以投资计划说明书中约定为准。发行人通讯地址、联系方式发生变更的，发行人可自行选择以本合同规定的任一信息披露方式披露。</p>
		<p style="text-indent:2em"><b>（二）通知的送达</b></p>
		<p style="text-indent:2em">认购人以专人送达、挂号信、传真、特快专递、电子邮件的方式，就处理投资事务过程中需要通知的事项通知本合同各方，通知在下列日期视为送达被通知方：</p>
		<p style="text-indent:2em">（1）专人送达：通知方取得的被通知方签收单所示日。</p>
		<p style="text-indent:2em">（2）挂号信邮递：发出通知方持有的国内挂号函件收据所示日后第5日</p>
		<p style="text-indent:2em">（3）传真：收到成功发送确认之日。</p>
		<p style="text-indent:2em">（4）特快专递：发出通知方持有的发送凭证上邮戳日起第4日。</p>
		<p style="text-indent:2em">（5）电子邮件：发件人邮件系统显示已成功发送之日。</p>
		<p style="text-indent:2em">发行人选择以专人送达、挂号信、传真、特快专递、电子邮件的方式就处理投资事务过程中需要通知的事项通知认购人的，通知送达日期的确定适用上一款的规定。</p>
		<p style="text-indent:2em">任何一方的通讯地址、电子邮箱和/或传真号码中的一项或多项发生变更，均应立即按照本合同规定以书面形式通知另一方；任何一方未就其通讯地址、电子邮箱、和/或传真号码中一项或多项的变更按照本条规定立即通知另一方的，另一方按照变更一方变更前的通讯地址和/或传真号码发送的书面通知，在发生本条规定情形时即视为有效送达，变更一方应自行承担因此而导致的任何法律、经济责任。</p>
		<br>
		<p style="text-align:center;font-weight:bolder">十六、适用法律与争议处理</p>
		<p style="text-indent:2em">本合同的订立、生效、履行、解释、修改和终止等事项适用中国法律。</p>
		<p style="text-indent:2em">与本合同有关的任何争议，各方应友好协商解决；若协商不能解决，任何一方均有权就有关争议向认购人住所地有管辖权的人民法院提起诉讼。</p>
		<p style="text-indent:2em">除双方发生争议的事项外，双方仍应当本着善意的原则按照本合同的规定继续履行各自义务。</p>
